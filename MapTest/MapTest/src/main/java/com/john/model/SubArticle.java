package com.john.model;

public class SubArticle {
	String sub_date;
	String sub_a;
	String sub_b;
	String sub_content;
	String sub_image;
	public String getSub_date() {
		return sub_date;
	}
	public void setSub_date(String sub_date) {
		this.sub_date = sub_date;
	}
	public String getSub_a() {
		return sub_a;
	}
	public void setSub_a(String sub_a) {
		this.sub_a = sub_a;
	}
	public String getSub_b() {
		return sub_b;
	}
	public void setSub_b(String sub_b) {
		this.sub_b = sub_b;
	}
	public String getSub_content() {
		return sub_content;
	}
	public void setSub_content(String sub_content) {
		this.sub_content = sub_content;
	}
	public String getSub_image() {
		return sub_image;
	}
	public void setSub_image(String sub_image) {
		this.sub_image = sub_image;
	}
}
